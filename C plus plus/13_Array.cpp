#include <iostream>
using namespace std;

int main()
{
    // creating array
    // Defining an array
    // Without specifying the size of the array:
    int arr[] = {1, 2, 3};

    // With specifying the size of the array:
    int arr[3];
    arr[0] = 1, arr[1] = 2, arr[2] = 3;

    // Accessing an array element
    int arr[] = {1, 2, 3};
    cout << arr[1] << endl;

    // Changing an array element
    int arr[] = {1, 2, 3};
    arr[2] = 8; // changing the element on index 2
    cout << arr[2] << endl;
  

    
}
